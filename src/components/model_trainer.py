def evaluate_models(X_train, y_train, X_test, y_test, models: dict):
    """
    Trains and evaluates each model using R2 score.
    Returns a dictionary with model name and its R2 score.
    """
    model_report = {}

    for model_name, model in models.items():
        model.fit(X_train, y_train)
        y_pred = model.predict(X_test)

        score = r2_score(y_test, y_pred)
        model_report[model_name] = score

    return model_report


@dataclass
class ModelTrainerConfig:
    trained_model_file_path=os.path.join("artifacts","model.pkl")
class ModelTrainer:
    def __init__(self):
        self.model_trainer_config=ModelTrainerConfig()

    def initiate_model_trainer(self,train_array,test_array,):
        try: 
             logging.info("Split training and test input data")
             X_train,y_train,X_test,y_test=(
                 train_array[:,:-1],
                 train_array[:,-1],
                 test_array[:,:-1],
                 test_array[:,-1]

             )
             models = {
                 "Random Forest": RandomForestRegressor(),
                 "Decision Tree": DecisionTreeRegressor(),
                 "Gradient Boosting": GradientBoostingRegressor(),
                 "Linear Regressor": LinearRegression(),
                 "K-Neighbors Classifier": CatBoostRegressor(verbose=False),
                 "AdaBoost Classifier": AdaBoostRegressor(),
             }

             model_report:dict=evaluate_models(X_train=X_train,y_train=y_train,X_test=X_test,y_test=y_test,models=models)

             ## To get best model name for  dict
             best_model_score = max(sorted(model_report.values())) 
             ## To get best model name for dict
             best_model_name = list(model_report.keys())[
                 list(model_report.values()).index(best_model_score)
             ]
             best_model = models[best_model_name]

             if best_model_score<0.6:
                 raise CustomException("No best model found")
             logging.info(f"Best found model on both trainingg and testing dataset") 

             save_object(
                 file_path=self.model_trainer_config.trained_model_file_path,obj=best_model
             )

             predicted=best_model.predict(X_test)

             r2_square = r2_score(y_test, predicted)
             return r2_square





            
        except Exception as e:
            raise CustomException(e,sys)
            

